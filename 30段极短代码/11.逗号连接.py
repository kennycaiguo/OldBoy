"""
下面的代码可以将列表连接成单个字符串，且每一个元素间的分隔方式设置为了逗号
"""


hobbies = ["basketball", "football", "swimming"]

print("My hobbies are: " + ", ".join(hobbies))
# My hobbies are: basketball, football, swimming
